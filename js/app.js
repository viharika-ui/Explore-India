function selectState(stateName) {
  localStorage.setItem("selectedState", stateName);
  window.location.href = "places.html";
}

function loadPlaces() {
  const state = localStorage.getItem("selectedState");

  if (!state || !indiaData[state]) {
    console.error("Invalid or missing state");
    return;
  }

  const title = document.getElementById("stateTitle");
  const list = document.getElementById("placesList");
  const searchInput = document.getElementById("searchInput");

  title.innerText = state;

  function renderPlaces(filterText = "") {
    list.innerHTML = "";

    indiaData[state]
      .filter(place =>
        place.name.toLowerCase().includes(filterText.toLowerCase())
      )
      .forEach(place => {
        const li = document.createElement("li");
        li.innerText = place.name;

        li.onclick = () => {
          localStorage.setItem(
            "selectedPlace",
            JSON.stringify(place)
          );
          window.location.href = "placedetails.html";
        };

        list.appendChild(li);
      });

    if (list.innerHTML === "") {
      list.innerHTML = "<li>No places found</li>";
    }
  }

  renderPlaces();

  if (searchInput) {
    searchInput.addEventListener("input", (e) => {
      renderPlaces(e.target.value);
    });
  }
}

function loadPlaceDetails() {
  const place = JSON.parse(localStorage.getItem("selectedPlace"));

  if (!place) {
    console.error("No place data found");
    return;
  }

  document.getElementById("placeName").innerText = place.name;
  document.getElementById("timings").innerText = place.timings;
  document.getElementById("station").innerText = place.station;
  document.getElementById("distance").innerText = place.distance;
  document.getElementById("transport").innerText = place.transport;
  document.getElementById("details").innerText = place.details;
  document.getElementById("placeImage").src = place.image;
  document.getElementById("placeImage").alt = place.name;

}